<script lang="ts" setup>
onMounted(() => {
  function updateTheme (theme: string) {
    document.documentElement.setAttribute('data-theme', theme)
    theme === '' ? localStorage.removeItem('theme') : localStorage.setItem('theme', theme)
  }

  const defaultTheme = localStorage.getItem('theme') || (window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light')
  updateTheme(defaultTheme)
  const buttons = document.querySelectorAll('.theme-selector')
  buttons.forEach((btn) => {
    btn.addEventListener('click', function (this: Element) {
      const selectedTheme = this.getAttribute('data-set-theme') || ''
      updateTheme(selectedTheme)
    })
  })
})
</script>
